// Utility functions
export const getStatusColor = (status: string): string => {
  return statusColors[status as keyof typeof statusColors] || statusColors.offline;
};

export const getThreatColor = (level: string): string => {
  return threatColors[level as keyof typeof threatColors] || threatColors.info;
};

export const getSeverityColor = (severity: string): string => {
  switch (severity) {
    case 'info':
      return colors.info.main;
    case 'warning':
      return colors.warning.main;
    case 'error':
      return colors.error.main;
    case 'critical':
      return colors.error.dark;
    default:
      return colors.grey[500];
  }
};

